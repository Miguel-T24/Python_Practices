# 9. Write a python function that takes a numbers as a parameter and checks wheter the number is prime or not

def test_prime(n):
    if (n==1):
        return False
    elif (n==2):
        return True;
    else:
        for x in range(2,n):
            if(n % x==0):
                return False
        return True             
print(test_prime(9))